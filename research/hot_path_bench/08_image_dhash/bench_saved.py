#!/usr/bin/env python3
"""Benchmark: dHash on Saved Test Images

Loads the images generated by generate_images.py and computes dHash
for each, verifying that:
  - Same photo variants produce similar hashes (d ≤ 10)
  - Different photos produce different hashes (d > 10)

Usage:
  python generate_images.py    # first time only
  python bench_saved.py
"""

import sys, os
sys.path.insert(0, os.path.join(os.path.dirname(__file__), "../../.."))
from src.skills.image_phash import image_phash

IMG_DIR = os.path.join(os.path.dirname(__file__), "test_images")
ORIGINALS = f"{IMG_DIR}/originals"
VARIANTS = f"{IMG_DIR}/variants"


def load_image(path: str) -> bytes:
    with open(path, "rb") as f:
        return f.read()


def hash_file(path: str) -> str:
    return image_phash.dhash(load_image(path))


def main():
    print("=" * 70)
    print("dHash Benchmark on Saved Test Images")
    print("=" * 70)

    # ---- Load originals ----
    print(f"\n--- Originals ({ORIGINALS}) ---")
    original_files = sorted(f for f in os.listdir(ORIGINALS) if f.endswith((".png", ".jpg")))
    originals = {}
    for fname in original_files:
        path = f"{ORIGINALS}/{fname}"
        h = hash_file(path)
        originals[fname] = h
        print(f"  {fname:25s}: {h}")

    # ---- Load variants of landscape_A ----
    print(f"\n--- Variants of landscape_A ({VARIANTS}) ---")
    variant_files = sorted(f for f in os.listdir(VARIANTS) if f.endswith((".png", ".jpg")))
    ref = originals["landscape_A.png"]
    print(f"  Reference: landscape_A.png = {ref}\n")

    all_variants_pass = True
    for fname in variant_files:
        path = f"{VARIANTS}/{fname}"
        h = hash_file(path)
        d = image_phash.hamming(ref, h)
        status = "✓" if d <= 10 else "✗ FAIL"
        if d > 10:
            all_variants_pass = False
        bar = "█" * min(d, 40) + "░" * max(0, 40 - min(d, 40))
        print(f"  [{status}] {fname:30s}: d={d:>3}/64  {bar}")

    # ---- Cross-check between different originals ----
    print(f"\n--- Cross-check: Different Photos ---")
    names = sorted(originals.keys())
    all_different_pass = True
    for i, n1 in enumerate(names):
        for n2 in names[i + 1:]:
            d = image_phash.hamming(originals[n1], originals[n2])
            status = "✓" if d > 10 else "✗ FALSE MATCH!"
            if d <= 10:
                all_different_pass = False
            print(f"  [{status}] {n1:25s} ↔ {n2:25s}: d={d:>3}/64")

    # ---- Summary ----
    print(f"\n{'='*70}")
    print("Summary")
    print(f"{'='*70}")
    print(f"  Same-photo variants (should d≤10): {'ALL PASSED' if all_variants_pass else 'SOME FAILED'}")
    print(f"  Different photos  (should d>10):  {'ALL PASSED' if all_different_pass else 'FALSE MATCHES'}")
    print(f"  Threshold: Hamming distance ≤ 10")

    # Stats
    variant_dists = []
    for fname in variant_files:
        d = image_phash.hamming(ref, hash_file(f"{VARIANTS}/{fname}"))
        variant_dists.append(d)
    cross_dists = []
    for i, n1 in enumerate(names):
        for n2 in names[i + 1:]:
            cross_dists.append(image_phash.hamming(originals[n1], originals[n2]))

    print(f"\n  Variant distances:   min={min(variant_dists)} max={max(variant_dists)} avg={sum(variant_dists)/len(variant_dists):.1f}")
    print(f"  Cross distances:     min={min(cross_dists)} max={max(cross_dists)} avg={sum(cross_dists)/len(cross_dists):.1f}")
    print(f"  Separation margin:   {min(cross_dists) - max(variant_dists)} (positive = safe)")


if __name__ == "__main__":
    main()
